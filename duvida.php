                        <p style="text-align: center"><p style="text-align:justify"><span style="font-size:16px"><span style="color:#000000"><strong>Quais as condi&ccedil;&otilde;es para alugar um carro?&nbsp;</strong><br />
<br />
Se<strong>&nbsp;</strong><strong>pessoa f&iacute;sica</strong>, basta que apresente documento de identidade ou passaporte, seja maior de 21 (vinte e um) anos, tenha habilita&ccedil;&atilde;o v&aacute;lida no Brasil emitida h&aacute; mais de 2(dois) anos, possua um cart&atilde;o de cr&eacute;dito ou tenha o cadastro previamente aprovado pela empresa.&nbsp;Condutores&nbsp;estrangeiros dever&atilde;o apresentar tradu&ccedil;&atilde;o juramentada da habilita&ccedil;&atilde;o (permiss&atilde;o para dirigir), ou a carteira de habilita&ccedil;&atilde;o internacional.&nbsp;<br />
<br />
Se&nbsp;<strong>pessoa jur&iacute;dica</strong>, &eacute; necess&aacute;rio que a empresa tenha mais de um ano de constitu&iacute;da, apresente as informa&ccedil;&otilde;es e a documenta&ccedil;&atilde;o&nbsp;exigidas&nbsp;e tenha o cadastro previamente aprovado pela empresa, o que ap&oacute;s a an&aacute;lise&nbsp;cadastral ser&aacute; comunicado&nbsp;em at&eacute; 2(dois) dias &uacute;teis.&nbsp;&Eacute; necess&aacute;rio tamb&eacute;m que indique o condutor do ve&iacute;culo.<br />
<br />
<br />
<strong>Posso indicar um outro condutor para o ve&iacute;culo alugado?</strong><br />
<br />
Sim. Desde que seja maior de 21(vinte e um)&nbsp;anos e tenha mais de 2(dois) anos de habilitado, bastando que seja informado na abertura do contrato o&nbsp;nome e endere&ccedil;o do condutor adicional, e apresentado c&oacute;pia da identidade e habilita&ccedil;&atilde;o.&nbsp;<br />
<br />
<br />
<strong>Posso alugar&nbsp;um carro com o motorista?</strong><br />
<br />
Sim.&nbsp;Desde que solicitado antecipadamente,&nbsp;o carro pode ser alugado com motorista mediante o&nbsp;pagamento de taxa di&aacute;ria e dos demais valores contratados.</span></span></p>

<p style="text-align:justify"><br />
<span style="font-size:16px"><span style="color:#000000"><strong>Posso escolher a marca ou o modelo do carro de minha prefer&ecirc;ncia?</strong><br />
<br />
Sim. Mas a reserva estar&aacute; condicionada a disponibilidade do modelo escolhido, j&aacute; que o atendimento &eacute; por Grupo de ve&iacute;culos e n&atilde;o por modelo. No entanto faremos todo o poss&iacute;vel para atend&ecirc;-lo com o ve&iacute;culo de sua prefer&ecirc;ncia.</span></span></p>

<p style="text-align:justify"><br />
<br />
<span style="font-size:16px"><span style="color:#000000"><strong>Posso apanhar ou devolver o ve&iacute;culo fora do hor&aacute;rio de funcionamento da Locadora?</strong><br />
<br />
Sim. Desde que previamente acertado com um dos nossos operadores no momento da reserva ou da entrega do ve&iacute;culo,&nbsp;e pagamento do valor da taxa de entrega e/ou devolu&ccedil;&atilde;o, se&nbsp;devido.</span></span></p>

<p style="text-align:justify"><br />
<span style="font-size:16px"><span style="color:#000000"><strong><strong>Posso apanhar e/ou devolver o ve&iacute;culo no&nbsp;hotel,&nbsp;local de trabalho ou domic&iacute;lio do Locat&aacute;rio?</strong></strong><br />
<br />
Sim.&nbsp;Quando&nbsp;a entrega&nbsp;ou devolu&ccedil;&atilde;o for dentro do per&iacute;metro urbano do Rio de Janeiro-RJ e Natal-RN, n&atilde;o h&aacute; cobran&ccedil;a de taxa. Caso&nbsp;a entrega seja&nbsp;ou fora do per&iacute;metro urbano,&nbsp;ser&aacute; cobrado&nbsp;taxa, que ser&aacute; dispensada quando&nbsp;a loca&ccedil;&atilde;o for a partir&nbsp;de tr&ecirc;s dias seguidos. Desde que n&atilde;o ocorra aos domingos e feriados. Se o ve&iacute;culo&nbsp;for entregue ou devolvido no hor&aacute;rio entre 21:01hs e 06:59hs, ser&aacute; sempre cobrado&nbsp;taxa de R$ 50,00 para entrega e/ou devolu&ccedil;&atilde;o,&nbsp;independentemente do n&uacute;mero de di&aacute;rias da loca&ccedil;&atilde;o.</span></span></p>

<p style="text-align:justify"><br />
<span style="font-size:16px"><span style="color:#000000"><strong>Posso apanhar e devolver o ve&iacute;culo no Aeroporto?</strong><br />
<br />
Sim. Mediante pagamento de taxa de entrega e devolu&ccedil;&atilde;o, sendo dispensada se a loca&ccedil;&atilde;o for a partir&nbsp;de tr&ecirc;s dias consecutivos, desde que n&atilde;o ocorra aos domingos e feriados. Se o ve&iacute;culo for entregue ou devolvido entre 20:01hs e 05:59hs, independentemente do&nbsp;n&uacute;mero de&nbsp;dias de loca&ccedil;&atilde;o, ser&aacute;&nbsp;cobrado taxa, a ser consultada, para cada percurso. Um dos nosso operadores devidamente uniformizado, estar&aacute; aguardando o Locat&aacute;rio na &aacute;rea do desembarque, com a identifica&ccedil;&atilde;o&nbsp;do nome&nbsp;e o voo informado.</span></span><br />
<br />
<br />
<br />
<span style="font-size:16px"><span style="color:#000000"><strong>Tenho que devolver o ve&iacute;culo com o tanque de combust&iacute;vel cheio?</strong><br />
<br />
N&atilde;o. Voc&ecirc; dever&aacute; devolver o carro nas mesmas condi&ccedil;&otilde;es em que foi entregue, mas para sua&nbsp;comodidade,&nbsp;a empresa entrega&nbsp;com o tanque cheio e voc&ecirc; devolve nas mesmas condi&ccedil;&otilde;es. Caso&nbsp;o ve&iacute;culo&nbsp;seja devolvido com o n&iacute;vel do tanque abaixo do que foi entregue,&nbsp;o combust&iacute;vel consumido&nbsp;ser&aacute; cobrado&nbsp;do Locat&aacute;rio acrescido&nbsp;de taxa&nbsp;de 20%, calculado com base na leitura do marcador medido em oitavos.<br />
Por se tratar de uma leitura com quantidades&nbsp;aproximadas, o valor cobrado&nbsp;do combust&iacute;vel poder&aacute; sofrer varia&ccedil;&atilde;o&nbsp;para mais ou para menos,&nbsp;em rela&ccedil;&atilde;o ao posto de abastecimento.<br />
<br />
<br />
<strong>Como s&atilde;o apropriadas as di&aacute;rias do ve&iacute;culo locado?</strong><br />
<br />
A di&aacute;ria do ve&iacute;culo &eacute; de 24(vinte e quatro)&nbsp;horas com 1(uma) hora de toler&acirc;ncia, ap&oacute;s o que incide&nbsp;horas extras a raz&atilde;o de 1/6 do valor da di&aacute;ria, inclusive sobre a hora&nbsp;tolerada, e&nbsp;ap&oacute;s a 6a. hora ser&aacute; cobrada nova&nbsp;di&aacute;ria&nbsp;e nova taxa de cobertura de risco.&nbsp;<br />
<br />
<br />
<strong>O que &eacute; uma Cau&ccedil;&atilde;o?</strong><br />
<br />
&Eacute; um valor determinado que &eacute; oferecido pelo Locat&aacute;rio para garantia de posse do ve&iacute;culo e antecipa&ccedil;&atilde;o do pagamento parcial da loca&ccedil;&atilde;o, devendo preferencialmente ser em cart&atilde;o de cr&eacute;dito, o que agiliza a pesquisa e a aprova&ccedil;&atilde;o&nbsp;do cadastro. O valor da cau&ccedil;&atilde;o depende do Grupo do ve&iacute;culo reservado.<br />
<br />
<br />
<strong>O que &eacute; uma Pr&eacute;-Autoriza&ccedil;&atilde;o?</strong><br />
<br />
&Eacute; o bloqueio tempor&aacute;rio no cart&atilde;o de cr&eacute;dito do Locat&aacute;rio de uma quantia pr&eacute;-determinada, para garantia&nbsp;da reserva e antecipa&ccedil;&atilde;o do pagamento parcial da loca&ccedil;&atilde;o. A autoriza&ccedil;&atilde;o pela administradora do cart&atilde;o indica que&nbsp;o Locat&aacute;rio tem cr&eacute;dito suficiente&nbsp;para o pagamento dos&nbsp;d&eacute;bitos com a Locadora at&eacute; o valor pr&eacute;-autorizado.&nbsp;&middot;&nbsp;A pr&eacute;-autoriza&ccedil;&atilde;o, &eacute; solicitada da administradora do cart&atilde;o quando da reserva ou abertura do contrato, sendo cancelada ao final da loca&ccedil;&atilde;o, ap&oacute;s a confirma&ccedil;&atilde;o da autoriza&ccedil;&atilde;o do valor do d&eacute;bito devido ao pagamento da loca&ccedil;&atilde;o.<br />
<br />
<br />
<strong>O que &eacute; Cobertura de Risco?</strong></span></span><br />
<br />
<span style="color:#000000; font-size:16px; line-height:1.6">A Cobertura de Risco&nbsp;</span><strong style="color:#000000; font-size:16px; line-height:1.6">n&atilde;o &eacute; um seguro</strong><span style="color:#000000; font-size:16px; line-height:1.6">, nas uma&nbsp;</span><strong style="color:#000000; font-size:16px; line-height:1.6">prote&ccedil;&atilde;o</strong><span style="color:#000000; font-size:16px; line-height:1.6">&nbsp;opcional oferecida, que isenta parcialmente o Locat&aacute;rio das responsabilidades financeiras&nbsp;devido a colis&atilde;o ou perda total do ve&iacute;culo locado, nas condi&ccedil;&otilde;es contratadas.&nbsp;</span></p>

<p style="text-align:justify"><br />
<br />
<span style="font-size:16px"><span style="color:#000000"><strong>O que &eacute; Lucro Cessante?</strong></span></span></p>

<p style="text-align:justify"><span style="font-size:16px"><span style="color:#000000">&Eacute; o preju&iacute;zo sofrido&nbsp;em decorr&ecirc;ncia de acidente, pane, quebra&nbsp;ou perda total do ve&iacute;culo, quando&nbsp;a Locadora&nbsp;perde ou deixa de lucrar&nbsp;com a loca&ccedil;&atilde;o&nbsp;do ve&iacute;culo,&nbsp;nos dias em&nbsp;que o mesmo&nbsp;fica&nbsp;sem condi&ccedil;&otilde;es de ser alugado por se encontrar, em reparo na oficina ou aguardando libera&ccedil;&atilde;o de seguradora,&nbsp;autoridade&nbsp;competente ou substitui&ccedil;&atilde;o por um outro ve&iacute;culo em caso de perda total.&nbsp;<br />
<br />
<br />
<strong><strong>Q</strong></strong><strong>uais as Coberturas de Risco oferecidas pela empresa?</strong><br />
<br />
<strong>Cobertura Parcial</strong><br />
Prote&ccedil;&atilde;o&nbsp;<strong>somente para o ve&iacute;culo</strong>, em caso de avaria por colis&atilde;o ou perda total por sinistro, inc&ecirc;ndio, furto ou roubo, assumindo o Locat&aacute;rio sua coparticipa&ccedil;&atilde;o obrigat&oacute;ria (franquia) nos valores especificados no contrato, al&eacute;m de assumir os custos referentes aos lucros cessantes, n&atilde;o estando contemplado&nbsp;qualquer cobertura para terceiros<strong>.</strong><strong>&nbsp;</strong></span></span><br />
<br />
<br />
<br />
<span style="font-size:16px"><span style="color:#000000"><strong>&Eacute; obrigat&oacute;rio apresentar o Boletim de Ocorr&ecirc;ncia Policial?</strong>&nbsp;<br />
<br />
Sim. Em&nbsp;caso de ocorr&ecirc;ncia de colis&atilde;o, sinistro, furto, roubo ou inc&ecirc;ndio, &eacute; obrigat&oacute;rio que voc&ecirc;&nbsp;preencha o Relat&oacute;rio de Acidentes da empresa e que apresente&nbsp;o Boletim de Ocorr&ecirc;ncia Policial (BO), sem o que assumir&aacute; integralmente as despesas pelos danos causados ao ve&iacute;culo locado&nbsp;ou&nbsp;sua substitui&ccedil;&atilde;o&nbsp;por perda total at&eacute;&nbsp;o valor&nbsp;equivalente ao seu pre&ccedil;o de mercado conforme a Tabela FIPE, al&eacute;m dos custos ocasionados por danos a terceiros.<br />
<br />
<strong>Tenho Cobertura de Risco para vidros, pneus, som e acess&oacute;rios do ve&iacute;culo?</strong><br />
<br />
N&atilde;o. Avarias ou roubo de&nbsp;vidros, pneus, som&nbsp;e acess&oacute;rios n&atilde;o est&atilde;o contemplados nas coberturas de risco contratada,&nbsp;e em caso de qualquer ocorr&ecirc;ncia, o &ocirc;nus &eacute;&nbsp;de responsabilidade do locat&aacute;rio.</span></span><br />
<br />
<br />
<strong style="color:#000000; font-size:16px; line-height:1.6"><strong>Como devo proceder em caso de Acidente, Sinistro, Roubo,&nbsp;Pane&nbsp;ou Quebra do ve&iacute;culo?</strong></strong></p>

<p style="text-align:justify"><br />
<span style="font-size:16px"><span style="color:#000000">Em caso de&nbsp;<strong>acidente</strong>,&nbsp;procure a autoridade competente,&nbsp;fa&ccedil;a o registro do fato e&nbsp;providencie o Boletim de Ocorr&ecirc;ncia Policial, comunique de imediato a Locadora e&nbsp;preencha o formul&aacute;rio de Relat&oacute;rio de Acidentes da Locadora.<br />
Em caso de&nbsp;<strong>furto ou roubo</strong>&nbsp;procure a autoridade competente ou a delegacia mais pr&oacute;xima, para&nbsp;registrar a ocorr&ecirc;ncia,&nbsp;comunique de imediato o fato a Locadora,&nbsp;providencie o Boletim de Ocorr&ecirc;ncia Policial e preencha o formul&aacute;rio de Relat&oacute;rio de Acidentes da Locadora. As chaves e os documentos do ve&iacute;culo dever&atilde;o ser devolvidos a Locadora.<br />
Em caso de&nbsp;<strong>pane ou quebra</strong>, comunique de imediato a Locadora que tomar&aacute; as provid&ecirc;ncias para reparo, ou substitui&ccedil;&atilde;o do ve&iacute;culo em caso de defeito decorrente de uso normal.</span></span></p>

<p style="text-align:justify"><br />
<span style="font-size:16px"><span style="color:#000000"><strong>Sou respons&aacute;vel pelo pagamento de multa em caso de cometimento de infra&ccedil;&atilde;o de tr&acirc;nsito?</strong><br />
<br />
Sim. As multas devidas a condu&ccedil;&atilde;o do ve&iacute;culo s&atilde;o de responsabilidade do Locat&aacute;rio ou do condutor indicado no contrato de loca&ccedil;&atilde;o.&nbsp;&middot;&nbsp;Conforme determina o C&oacute;digo de Tr&acirc;nsito, em caso de notifica&ccedil;&atilde;o de infra&ccedil;&atilde;o, o Locat&aacute;rio ser&aacute; apresentado como infrator ao &oacute;rg&atilde;o competente,&nbsp;a quem a Locadora pagar&aacute; diretamente o valor da multa, sendo posteriormente cobrado do Locat&aacute;rio atrav&eacute;s do cart&atilde;o de cr&eacute;dito, faturamento ou reembolso.</span></span></p>

<p style="text-align:justify"><br />
<span style="font-size:16px"><span style="color:#000000"><strong>Como procedo em caso de autua&ccedil;&atilde;o e notifica&ccedil;&atilde;o pr infra&ccedil;&atilde;o&nbsp;de tr&acirc;nsito?</strong><br />
<br />
Em caso do condutor&nbsp;ser diretamente autuado pelo agente de tr&acirc;nsito, o Locat&aacute;rio dever&aacute; apresentar a notifica&ccedil;&atilde;o a Locadora, para as provid&ecirc;ncias necess&aacute;rias a indica&ccedil;&atilde;o do infrator e pagamento da multa.</span></span></p>
